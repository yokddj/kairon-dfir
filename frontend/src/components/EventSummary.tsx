    pushIf(rows, "Severity", detection.severity ?? eventMeta.severity);
    pushIf(rows, "Category", detection.category);
    pushIf(rows, "Action", detection.action ?? eventMeta.action);
    pushIf(rows, "Status", detection.status);
    pushIf(rows, "Remediation action", detection.remediation_action);
    pushIf(rows, "Detection source", detection.detection_source);
    pushIf(rows, "Resource", detection.resource);
