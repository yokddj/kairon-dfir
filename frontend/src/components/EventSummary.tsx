    pushIf(rows, "Transfer completion", bits.transfer_completion_time);
    pushIf(rows, "Expiration time", bits.expiration_time);
    pushIf(rows, "Notify command", bits.notify_cmd_line);
    pushIf(rows, "Notify flags", bits.notify_flags);
