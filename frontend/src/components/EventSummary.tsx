    pushIf(rows, "No progress timeout", bits.no_progress_timeout);
    pushIf(rows, "Minimum retry delay", bits.minimum_retry_delay);
    pushIf(rows, "Error code", bits.error_code);
    pushIf(rows, "Error description", bits.error_description);
    pushIf(rows, "Parser status", bits.parser_status);
    pushIf(rows, "Source file", bits.source_file ?? event.source_file);
