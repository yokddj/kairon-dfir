    pushIf(rows, "Indexed path", windowsSearch.indexed_path);
    pushIf(rows, "Content type", windowsSearch.content_type);
    pushIf(rows, "Indexed last modified", windowsSearch.last_modified);
    pushIf(rows, "Office app", office.app);
    pushIf(rows, "Office alert", office.alert_text);
    pushIf(rows, "Office document", office.document_path);
